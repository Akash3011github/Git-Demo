package com.collection;

import java.util.HashSet;
import java.util.Set;

public class HashSetDemo {

	
	
	
	
    public static void main(String []args)
    {
    	
    	Set<String> set =new HashSet<String>();
    	
    	
    	set.add("Akash");
    	set.add("Sujal");
    	set.add("Pravin");
    	set.add("Vishal");
    	set.add("Prasad");
    	
    	
    	
    	for(String str: set) {
    	
    	
    	
    	System.out.println(str);
    	
    	
    	
    }
}}
